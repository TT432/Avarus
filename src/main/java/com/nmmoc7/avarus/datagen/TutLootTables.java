package com.nmmoc7.avarus.datagen;

import com.nmmoc7.avarus.block.AvarusBlocks;
import net.minecraft.data.DataGenerator;

/**
 * @author DustW
 **/
public class TutLootTables extends BaseLootTableProvider {

    public TutLootTables(DataGenerator dataGeneratorIn) {
        super(dataGeneratorIn);
    }

    @Override
    protected void addTables() {
        createSimpleTable("red_dirt", AvarusBlocks.RED_DIRT.get());
        createSimpleTable("green_dirt", AvarusBlocks.GREEN_DIRT.get());
        createSimpleTable("yellow_dirt", AvarusBlocks.YELLOW_DIRT.get());
        createSimpleTable("blue_dirt", AvarusBlocks.BLUE_DIRT.get());
    }
}